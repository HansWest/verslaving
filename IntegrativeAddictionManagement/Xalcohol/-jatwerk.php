  niet lekker voelen. Op die manier kun je de nare gevoelens die je hebt, tijdelijk 
  verdoven. De oorzaak van die gevoelens los je er niet mee op. Het kan moeilijk 
  zijn om in zo&quot;n situatie minder te drinken dan je je hebt voorgenomen. 
