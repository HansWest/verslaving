<H1>gejat:</H1></p>
<p>&nbsp;</p>
<p>Veel mensen vragen wat zij kunnen doen als hun partner, vriend, vriendin of 
  familielid te veel gebruikt of verslaafd is. De mensen in de omgeving van een 
  verslaafde hebben niet veel invloed op het gebruik of de verslaving. De gebruikers 
  bepalen zelf wat zij doen of laten. Toch is het voor ons mogelijk om tips te 
  noemen waarvan in de praktijk is gebleken dat mensen er wat aan hebben. <br>
  1. Dek het probleem niet toe <br>
  De nadelen van de verslaving voor vrienden en partners verdwijnen niet vanzelf. 
  In tegendeel. Familieleden en partners maken vaak excuses naar anderen of beschermen 
  de verslaafde tegen de gevolgen van zijn of zijn gebruik. Het is belangrijk 
  dat de verslaafde zelf de nadelige consequenties van zijn / haar gedrag ervaart. 
  Los de nadelige gevolgen van de verslaving niet op. Zolang de verslaafde persoon 
  geen nadelige gevolgen ondervindt, is er geen reden om te veranderen. Laat de 
  nadelige gevolgen maar komen, voorzover je dit praktisch of emotioneel voor 
  jezelf acceptabel vindt. Bovendien bestaat voor partners het risico dat zij 
  overbelast raken of contacten verliezen als zij niet met voor hun belangrijke 
  mensen over de moeilijke situatie kunnen spreken. <br>
  2. Probeer er op het juiste tijdstip over te beginnen<br>
  Het beste tijdstip om met een verslaafde over zijn / haar gebruik te beginnen, 
  is kort nadat er door dat gebruik weer een duidelijk probleem is ontstaan, zoals 
  bijvoorbeeld een ruzie of een ongeluk(je). Kies wel een tijdstip waarop hij 
  / zij niet onder invloed is, jullie beiden redelijk kalm zijn en de kans hebben 
  om er onder vier ogen over te praten. <br>
  3. Wees duidelijk<br>
  Vertel dat je je grote zorgen maakt over zijn / haar gedrag. Gebruik duidelijke 
  voorbeelden van de problemen die zijn ontstaan, vooral de meest recente. Heb 
  het vooral over de concreet merkbare gedragsmatige gevolgen van het gebruik 
  en niet zo zeer over het gebruik zelf. Bijvoorbeeld: dat jij ons kind vergeet 
  op te halen van school omdat je teveel blowt, kan ik niet accepteren'. En niet: 
  'Je moet niet zoveel blowen'. De eerste zin is concreet en goed benoembaar. 
  <br>
  4. Bepaal je grenzen en bescherm je eigen belangen <br>
  Het komt veel voor dat mensen die met een verslaafde samenleven / bevriend zijn 
  steeds meer van zichzelf gaan inleveren. Ze laten hun leven steeds meer door 
  de verslaving van de partner / vriend sturen. Bij het willen helpen van de partner 
  / vriend cijferen ze zichzelf weg en gaan ze steeds meer accepteren. Bedenk 
  wat jouw grenzen zijn en wat je nog aantrekkelijk in de relatie vindt. <br>
  5. Leg uit wat jijzelf van plan bent<br>
  Leg uit wat je van plan bent te doen als hij / zij geen hulp zoekt. Zeg dat 
  je dat niet doet om te straffen, maar om jezelf te beschermen tegen de gevolgen 
  van zijn / haar verslaving. Wat je van plan bent, kan dan vari&euml;ren. Roep 
  echter geen dingen die je toch niet kunt of wilt uitvoeren. <br>
  6. Geen constante strijd<br>
  Constante strijd over onacceptabel gedrag is zinloos. Zoek het evenwicht tussen 
  grenzen stellen aan gedrag enerzijds en het vermijden van constante strijd anderzijds. 
  Het helpt niet om constant te gaan drammen of om een enorme druk uit te oefenen. 
  Dat roept extra verzet en ontkenning op. <br>
  7. Los niet automatisch de nadelen van de verslaving op<br>
  Mensen komen pas in beweging als zij met de nadelen van hun gebruik worden geconfronteerd. 
  Als jij alles accepteert of oplost, maakt dat het voor de verslaafde gemakkelijker 
  om het gebruik op die wijze voort te zetten. Wij zeggen hiermee niet dat de 
  verslaafde in de goot moet komen en dat jij alles spaak moet laten lopen. Dat 
  kan consequenties hebben die je onaanvaardbaar vindt. We bedoelen hiermee wel 
  dat je niet automatisch alle nadelen van het gebruik moet oplossen. Als de gebruiker 
  door jouw constante oplossen van nadelen van het gebruik niet op de blaren hoeft 
  te zitten, voelt hij / zij ook geen pijn en is er weinig reden om te veranderen. 
  <br>
  8. Orienteer je op hulpverlening<br>
  Bekijk welke hulpverleningsmogelijkheden er in je omgeving zijn. Je kunt dit 
  ook met je huisarts bespreken. Als hij / zij bereid is om hulp te zoeken, kun 
  je aanbieden om mee te gaan op het eerste gesprek. Het kan soms helpen om iemand 
  op die manier over de drempel heen te helpen. Adressen van instellingen van 
  verslavingszorg vind je op onze pagina met links. <br>
  9. Zoek ook zelf ondersteuning, eventueel bij de verslavingszorg<br>
  Bespreek je situatie met mensen die voor jou belangrijk zijn. Maak er geen geheim 
  van. Dat voorkomt dat je vereenzaamt en dat je voor je zelf hulp en steun afhoudt. 
  Adressen van instellingen van verslavingszorg vind je op onze pagina met links. 
  <br>
  10. Zoek informatie over alcohol, drugs, verslaving<br>
  Als je informatie hebt over alcohol, drugs, middelen, verslaving heb je meer 
  begrip van de situatie waarin je verkeert. Je kunt die informatie vinden op 
  diverse sites te vinden op sites zoals<br>
  Brijder Verslavingszorg<br>
  Parnassia Verslavingszorg<br>
  Jellinek<br>
  Alcoholinfo.nl<br>
  Drugsinfo.nl<br>
  Meer sites op onze pagina met links. <br>
  Je kunt ook bellen met de Alcoholinfolijn: 0900-5002021 en de Drugsinfolijn: 
  0900-1995 (20c p/m).<br>
  Als je nog verdere vragen hebt, kun je deze bij Vraag &amp; Antwoord . op deze 
  site stellen. Mensen uit Noord-Holland (buiten Amsterdam en het Gooi) kunnen 
  hulp krijgen van Brijder Verslavingszorg, de instelling van deze site. Bel dan 
  072-5272803 en vraag dan naar informatie over infogroepen of ABC-hulpverlening 
  (ABC=Advies, Begeleiding, Consultatie).</p>
