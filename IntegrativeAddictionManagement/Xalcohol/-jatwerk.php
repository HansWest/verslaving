<p>Uitslag van de test<br>
  Hoeveel is veel?<br>
  U hebt het eerste gedeelte van de vragenlijst ingevuld. Uit uw antwoorden blijkt, 
  dat u zelf vindt dat u niet te veel drinkt. Als u minder dan 21 glazen per week 
  drinkt, drinkt u inderdaad niet meer dan de meeste mannen van uw leeftijd. U 
  weet zelf vermoedelijk wel of deze week een uitzondering was of niet.<br>
  Wat is verantwoord?<br>
  Alcoholgebruik en gezondheid hebben veel met elkaar te maken. Verantwoord alcoholgebruik 
  wil zeggen: het aantal glazen dat u zonder bezwaar voor uw gezondheid kunt drinken. 
  Voor gezonde volwassen mannen gelden de volgende richtlijnen.<br>
  1. Drink gemiddeld niet meer dan drie glazen alcohol per dag<br>
  Wanneer u gemiddeld niet meer dan drie glazen per dag drinkt, hoeft u zich geen 
  zorgen te maken voor uw gezondheid. Uit uw antwoorden wordt duidelijk, dat u 
  de afgelopen week onder dit gemiddelde van drie glazen alcohol per dag bent 
  gebleven. Qua gemiddelde drinkt u dus niet te veel.<br>
  2. Drink maximaal vijf glazen per keer<br>
  De gemiddelde hoeveelheid alcohol is iets anders dan de hoeveelheid alcohol 
  per keer. U geeft aan dat u de afgelopen week geen enkele keer meer dan vijf 
  glazen heeft gedronken. Dat is verstandig. Wist u dat veel alcohol ineens niet 
  gezond is? Het is aan te raden niet meer dan vijf glazen per gelegenheid te 
  drinken, zeker niet elke week. Overkomt dit u wel eens, dan kunt u uw lichaam 
  de tijd geven om te herstellen door twee aaneengesloten dagen helemaal geen 
  alcohol te drinken.<br>
  3. Drink ten minste twee dagen in de week geen alcohol<br>
  Alcohol drinken kan heel makkelijk een gewoonte worden. Juist omdat het vaak 
  naar aanleiding van iets gebeurt: om iets te vieren of om even ergens niet aan 
  te hoeven denken. Of uit gewoonte, bijvoorbeeld elke dag v&oacute;&oacute;r 
  het avondeten. Als u ten minste twee dagen per week niets drinkt, voorkomt u 
  dat u ongemerkt meer gaat drinken.<br>
  4. Pas op met alcohol in het verkeer<br>
  Als u met meer dan 0,5 promille alcohol in uw bloed aan het verkeer deelneemt 
  bent u strafbaar (2 a 3 glazen). Voor bestuurders die het rijbewijs korter dan 
  5 jaar hebben geldt zelfs een limiet van 0,2 promille (&eacute;&eacute;n standaardglas 
  alcohol). Dit geldt niet alleen voor autorijden, maar ook voor rijden op een 
  motor, brommer, scooter of fiets! Voorkom dat u meer drinkt dan u zich had voorgenomen 
  door geen alcohol te drinken als u het verkeer nog in moet.<br>
  Wel of niet minderen?<br>
  U geeft in uw antwoorden ook aan dat u van plan bent binnen een half jaar minder 
  alcohol te gaan drinken. Kennelijk speelt u met het idee uw alcoholgebruik wat 
  te minderen. Uw alcoholgebruik was de afgelopen week verantwoord, maar minder 
  gaan drinken kan natuurlijk nooit kwaad. Misschien heeft u behoefte aan advies 
  hoe u dit het beste kunt aanpakken.</p>
