<p>Alcohol en uw gezondheid<br>
  U hebt de afgelopen week minder dan 21 glazen gedronken. Ook hebt u geen grote 
  hoeveelheden (meer dan 5 glazen) in een keer gedronken. <br>
  Als u zich elke week hieraan houdt, is dat een verantwoord alcoholgebruik, maar 
  minder alcohol drinken is altijd goed. In hoeverre uw gezondheid verbetert, 
  is moeilijk te zeggen. Als u zich zorgen maakt over uw gezondheid, dan kunt 
  u het beste contact opnemen met uw huisarts.De binnenkant van de maag is met 
  slijmvlies bekleed om de maagwand te beschermen tegen maagzuur, dat nodig is 
  om voedsel te verteren. Alcoholgebruik kan tot gevolg hebben dat het slijmvlies 
  beschadigt. Deze ontsteking wordt gastritis genoemd en het kan maagpijn veroorzaken. 
  Alcoholgebruik kan ook leiden tot een maagzweer. Veel mensen weten niet dat 
  alcohol drinken vaak de oorzaak is van maagpijn. Op dit moment hebt u geen maagklachten. 
  Als we kijken naar de hoeveelheid die u drinkt, is het ook niet te verwachten 
  dat u daarvan maagklachten zult krijgen.Terug</p>
<p>&nbsp;</p>
<p><br>
  Alcoholhoudende drank is in de afgelopen jaren vergeleken met andere producten 
  niet zo gek veel duurder geworden. Gezien de hoeveelheid die u drinkt zult u 
  vermoedelijk niet zoveel geld besparen als u minder gaat drinken.<br>
  Het kan zijn dat u vooral buitenshuis drinkt. Dan kan het nog aardig in de papieren 
  lopen. Als dat zo is, doe dan eens het volgende: hou een week bij hoeveel u 
  aan alcohol uitgeeft, vooral als u uitgaat. Bereken eens wat u dat kost op jaarbasis 
  en bedenk wat u voor leuke andere dingen met dat geld zou kunnen doen.</p>
<p>Minderen: moeilijk of makkelijk?<br>
  Minder drinken lijkt u niet moeilijk, maar ook niet gemakkelijk. U bent wel 
  van plan te gaan minderen! Ik ben het met u eens dat minder drinken ook niet 
  echt gemakkelijk is. Hoe kunt u beginnen?<br>
  Een handige manier is om een &quot;alcoholdagboek&quot; bij te houden. Houd 
  een aantal weken bij hoeveel u dagelijks drinkt. Dat kan op een heel simpele 
  manier door bijvoorbeeld in uw agenda te zetten hoeveel (standaard)glazen u 
  op een dag hebt gedronken. Op die manier kunt u uw alcoholgebruik beter in de 
  hand houden. Doe ook uw voordeel met de tips die u hierna vindt.Terug</p>
