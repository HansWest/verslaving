<p>Alcohol: de voor- en nadelen<br>
  U geeft aan dat u zich een beetje moeilijker kunt ontspannen als u minder alcohol 
  zou drinken. Er zijn nogal wat mensen die alcohol drinken om zich te kunnen 
  ontspannen. Dat kan zijn na een dag hard werken, maar ook als iemand zich echt 
  gespannen of gestrest voelt.<br>
  Alcohol is eigenlijk een raar stofje. Het kan in kleine hoeveelheden (een glas) 
  inderdaad wat ontspannend werken. Maar al bij een paar glazen werkt alcohol 
  juist angst- en spanningsverhogend. Als u wat gespannen bent en een glas alcohol 
  drinkt, merkt u dus eerst dat plezierige ontspannende effect. Omdat u dat gevoel 
  vast wilt houden drinkt u meer, waardoor u zich weer gespannen kunt gaan voelen. 
  Dan heeft iemand al snel de neiging nog wat te drinken om dat prettige lome 
  gevoel weer op te roepen. Minder drinken leidt er dus toe dat u zich beter kunt 
  ontspannen.</p>
<p>Door (veel) alcohol te drinken kunt u behoorlijk moe worden. Vaak weten mensen 
  niet dat moeheid het gevolg van alcohol drinken kan zijn. Integendeel: ze denken 
  dat alcohol hen opkikkert. En gek genoeg klopt dat nog ook.<br>
  Als u moe thuis komt en u neemt een paar pilsjes of een paar glazen wijn, dan 
  voelt u zich minder moe. Dat komt omdat alcohol een verdovende werking heeft. 
  U bent nog steeds moe, maar u voelt het niet meer. Het kost energie om alcohol 
  te verwerken. Als de alcohol is uitgewerkt voelt u de moeheid (bijvoorbeeld 
  de dag erna) wel. Maar de meeste mensen hebben niet in de gaten dat deze vermoeidheid 
  door de alcohol komt.<br>
  Als u zich houdt aan de hoeveelheid die u in de afgelopen week gedronken hebt, 
  hoeft u zich hierover geen zorgen te maken.Terug</p>
<p>Alcohol en uw omgeving<br>
  &quot;Als ik minder ga drinken reageert mijn omgeving daar positief op&quot;, 
  zegt u. Misschien hebt u al ervaren dat uw partner positief reageert als u minder 
