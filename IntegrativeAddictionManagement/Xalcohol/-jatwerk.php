  Alcohol kan aanleiding geven tot ruzie. Partners vinden het vaak heel vervelend 
  als hun man (veel) drinkt. Zij maken zich daar ook erge zorgen over. Uw partner 
  zal dan ook zeker positief op u reageren als u uw alcoholgebruik vermindert. 
  Vaak verbetert de sfeer in huis voor iedereen, ook voor u!Terug</p>
