<b>Het doet zo'n pijn!!!</b><br>
Sommige mensen ervaren tijdens een afkick &quot;heftige pijnen&quot; 
in hun lichaam. Dat is zo...<br>
Toch zie je vaak dat diezelfde mensen bij een volgende afkick (want vaak is die 
pijn een reden om dan maar weer te gebruiken dus dan moet je toch &quot;nog maar 
weer een keer&quot; afkicken...), dat diezelfde mensen eigenlijk helemaal niet 
zoveel pijn ervaren. En dat terwijl zij er waarschijnlijk nog beroerder aan toe 
waren dan de eerste keer.<br>
Op de een of andere manier is het mij ervaring dat het met name mannen zijn die 
van de lichamelijke kant last hebben terwijl het bij vrouwen vaak wat eerder psycisch 
voelt. Dus daarom is deze tekst even een beetje op mannen gericht.<br>
Maar als jij geen last hebt van de lichamelijke kant van de afkick, sla deze tekst 
dan gerust over:<br>
&quot;Jongen jongen wat ben je aan het knokken tegen jezelf.&quot;<br>
Het is jammer dat je de twijfel iedere keer te ver laat komen om het nog te tackelen 
en dat je dan opnieuw moet beginnen.<br>
Het wordt iedere keer zwaarder op deze manier. Je gaat er iedere keer meer tegenop 
zien.<br>
Wat zwaarder geschut betreft...<br>
Er zijn mensen die zweren bij acupunctuur, zeker als je erin gelooft.<br>
Zijn er mensen waar je naartoe gaat op het moment dat je begint (!) et twijfelen?<br>
Heb je de dingen op een rij die je kan gaan doen om jezelf af te leiden?<br>
(en doe je ze ook?)<br>
Mag je kankeren van jezelf als je jezelf niet lekker voelt en heb je drie kwartier 
onder de douche gestaan voordat je er een neemt? Heb je de medicatie gebruikt 
die je met je arts hebt afgesproken ter ondersteuning van jouw ontwenning? Want 
dat soort dingen staat namelijk in je *noodplan*.<br>
Of, en dan komt er een hele interessante ook al is ie soms niet zo leuk, ben je 
jezelf misschien een beetje aan het gek maken?. Dat werkt soms zo bij mensen die 
opzien tegen een ontwenning.<br>
Ik maak eerst een olifant van een mug en vervolgens kan ik 'm alleen maar met 
een jachtgeweer doodschieten, ja. <br>
We hebben op HelpDisk.nl wel vaker gezien dat emoties zich kunnen vertalen in 
lichamelijke signalen. Zou die pijn voor iets anders, iets emotioneels kunnen 
staan. Iets niet-lichamelijks?<br>
Wat mis je als je niet gebruikt?<br>
Natuurlijk is het zo dat een lichaam dat aan het 'afkicken' is van regelmatig 
gebruik zich niet lekker voelt. Maar hoe verschrikkelijk is het nou werkelijk?<br>
Het wordt misschien tijd voor een paar surflessen.<br>
Gevoelens, zo ook trek, hebben een beginfase, een top en een eindfase.<br>
Het wordt tijd om die eens te gaan bijhouden.<br>
Pak er een papiertje bij en ga de tijd en de intensiteit eens bijhouden. Het gevoel 
in sterkte van 1 tot 10 en dan kijken hoe het opkomt, hoe lang de top duurt en 
hoe het weggaat. Dan weet je ook beter hoe lang die golven duren en kan je er 
ook beter rekening mee houden. Maak er eens een grafiekje van. Dat is ook wel 
handig omdat iets achteraf de hele tijd groot lijkt te zijn geweest maar dat je 
achteraf merkt dat er toch veel wissselingen in zitten. Is het bijvoorbeeld nou 
echt de hele tijd op &quot;10 is maximaal&quot; of is het eigenlijk gewoon een 
zeven-en-een-half. Genoeg om knap lastig te zijn maar waarschijnlijk toch onvoldoende 
om je weer het hele gedoe van een terugval op je nek te halen?<br>
Laat ik duidelijk zijn. Als er echt iets aan de hand is moet je als een haas naar 
de dokter. Als die niks vindt dan zal er niks lichamelijks wezen. Ook al is het 
heel, heel wel mogelijk dat je het lichamelijk voelt.<br>
Ik heb het vaak, vaak gezien dat iemand de trek die hij kreeg doordat ie twijfelde 
ging vertalen in een lichamelijk gevoel. De druk die je een beetje op je kop kunt 
voelen, de droge mond die er nou eenmaal bij hoort, de gevoeligheid van de huid 
die veel mensen hebben bij een afkick (huid zit vol zenuwen en dat zenuwstelsel 
moet toch een beetje in een nieuw evenwicht komen). Het zij allemaal dingen waar 
je heel veel aandacht naartoe kunt laten gaan, waar je veel gedachten over kunt 
hebben als &quot;het is toch verschrikkelijk dat ik dit nu voel... (en zou ik 
niet beter iets kunnen nuttigen zodat ik dit gevoel niet meer heb...)&quot;. Het 
kan ook iets zijn waar je over kunt denken: &quot;volgens mij hebben de meeste 
mensen wel wat lichamelijke ongemakjes als ze een nieuw evenwicht in hun leven 
moeten zoeken&quot;.<br>
In het eerste geval geeft het nieuwe twijfel of je zult gebruiken of niet. Het 
lichaam zal zich voorbereiden op gebruik en de trek versterkt waardoor het gevoel 
versterkt waardoor de twijfel versterkt waardoor het gevoel versterkt waardoor...<br>
In het tweede geval merk je dat je trek hebt, ga je effe onder de douche en ga 
je door met de dingen die wel belangrijk zijn.<br>
Sterkte met het loslaten van het gevoel!!!