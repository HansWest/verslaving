

<p>Sociaal</p>
<p>Wanneer ik aan mijzelf begin te werken dan zal ik ook aan mijn (sociale) omgeving 
  moeten werken. Wanneer ik lezingen geef voor mensen die willen werken met mensen 
  die last hebben van een verslaving, dan zeg ik regelmatig: &quot;werken aan 
  een ontwenning van iemand met een verslaving is ook werken aan de omgeving van 
  die verslaafde.&quot;<br>
  Dat is natuurlijk makkelijk praten tegen anderen. Maar ik denk dat het toch 
  ook iets is dat jij zelf zou kunnen beseffen wanneer jij aan de ontwenning van 
  jouw verslaving wilt werken.<br>
  Stel nou eens even dat ook jouw verslaving samen hangt met de manier waarop jij met jouw 
  omgeving omgaat... Misschien toch de moeite om daar eens even over na te denken 
  wat dat zou betekenen.<br>
  -Wat zal jij dan aan jouw omgeving moeten duidelijk maken?<br>
  -Wat hebben zij van jou te leren?<br>
  Hoe wil je omgaan met de mensen uit jouw omgeving?<br>
  Natuurlijk is het nu heel verleidelijk om te zeggen: &quot;zij moeten ... doen!&quot;<br>
  Maar sorry, zij moeten niks. Als zij iets moeten zullen ze dat alleen doen omdat 
  jij ze dat duidelijk maakt en ik raad je aan om hen dat duidelijk te maken zonder 
  dat je daarvoor middelengebruik of dreigend middelengebruik nodig hebt.<br>
  Deze pagina is er om een plek te hebben om daar een beetje over na te denken.<br>
  -Wat heb je nodig van die mensen in jouw omgeving? <br>
  -Denk je werkelijk dat zij ertoe in staat zij om dat ook te geven?<br>
  en zo ja:<br>
  -Hoe ga jij hen duidelijk maken hoe zij dat aan jou kunnen geven op een, voor 
  jou, juiste manier?<br>
  Als we het dan toch over de twee kanten van de medaille hebben:<br>
  -Wat kan jij van deze persoon leren?<br>
  -En hoe kan jij jezelf en die ander helpen om ervoor te zorgen dat die ander 
  jou deze les kan laten leren?<br>
  Geen eenvoudige vragen. Vandaar dat we hier pas wat verderop in het traject 
  mee komen.<br>
  Toch weten de meeste mensen eigenlijk toch wel zeker dat je het uiteindelijk niet zult trekken als je alleen maar aan jezelf 
  werkt zonder aan je relaties met anderen te werken. Dus dan kan je het maar 
  beter zo gericht mogelijk aanpakken :-)</p>
