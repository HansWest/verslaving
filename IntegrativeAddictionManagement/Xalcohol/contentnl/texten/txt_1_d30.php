<p>Stel je voor dat je nooit zou zijn gaan gebruiken. Hoe had je leven er dan 
  uitgezien?<br>
  Dit is voor veel mensen geen makkelijke vraag. Maar voor veel mensen is het 
  toch van belang om over deze vraag nog eens voor jezelf over na te denken en 
  jezelf even af te vragen wat voor jou het antwoord is.<br>
  Stel je voor dat je nooit zou zijn gaan gebruiken. Hoe had je leven er dan uitgezien?<br>
  En ook hier weer. Sta er even echt bij stil... Niet het makkelijke of brave 
  antwoord maar serieus. Wat heb jij gehad aan de drank die je hebt gebruikt. 
  hoe overspannen was je anders geworden (ook al ben je achteraf toch nog steeds 
  overspannen geraakt). Hoeveel nachten had je anders wakker gelegen van je werk, 
  hoeveel nachten had je anders wakker gelegen door relatieproblemen of opvoedingsproblemen 
  of herinneringen aan vroeger. Of hoeveel ruzies had je gemaakt als je niet de 
  mogelijkheid had gehad om ze weg te drinken. Hoe vaak had je dingen over je 
  kant laten gaan als je niet de moed had ingedronken om er wat van te zeggen? 
  Hoeveel sociale evenementen had je over moeten slaan of hoeveel mannen of vrouwen 
  had je niet gekust als je jezelf je de sociale moed niet had ingedronken?<br>
