De handen iets te doen geven<br>
Friemelen<br>
Een glas in de hannd, daar kan je zo heerlijk mee spelen. Je zet ineens een beetje 
een nonchalante houding neer met zo'n glas, losjes in de handen.<br>
Is er niet een speeltje waar jij jezelf iets mee om handen kunt geven? Dat Griekse 
kralenkettinkje, een stressballetje, en munt om tussen de vingers te laten glijden 
en balletje om als een goochelaar mee te spelen...<br>
Het zijn allemaal dingen waarmee u kunt friemelen.