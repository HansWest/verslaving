  brief laten genereren.  klik hier voor een <a href="../tools_huisarts.php" target="_self"  class="menubutton">huisartsbrief</a><BR>Misschien is er even wat extra medicatie nodig om de 
  eerste periode te overbruggen. Misschien kruipt er een serieuzere aandoening 
  onder jouw verslaving uit, dat kan soms het geval zijn. Maar in veruit de meeste 
  gevallen zie je dat mensen die hun ontwenning vol houden in de loop van een 
  paar weken, soms zelfs een paar dagen, zeggen dat zij zich veel rustiger voelen 
  en dat zij beter en rustiger slapen dan zij sinds lang hebben gedaan. Juist 
  de afwezigheid van de alcohol en de afbraakproducten van alcohol maken dat er 
  meer rust is.<br>
  Maar goed, misschien ben je daar nog niet aangeland...<br>
  Voel maar hoe je nu bezig bent om te ontgiften, sterkte als je slecht inslaapt 
  vannacht. Als dat zo is: het hoort er helaas voor veel mensen bij. Er zit vanavond 
  waarschijnlijk weinig anders op dan terugvallen of deze fase dan maar even uit 
  te houden, zo rustig mogelijk te blijven liggen en zo veel als mogelijk uit 
  te rusten. Ook al slaap je in de hele nacht nog geen kwartier, probeer zo veel 
  mogelijk uit te rusten. Rustige achtergrond muziek, zachte verlichting of juist 
  goede verdonkering en misschien een glas warme melk met honing kan je zo veel 
  mogelijk rust schenken. Adem en ontspanningsoefeningen kunnen soms uitkomst 
  brengen als je jezelf er goed op weet te concentreren hoe vermoeid jouw lichaam 
  eigenlijk voelt.</p>
