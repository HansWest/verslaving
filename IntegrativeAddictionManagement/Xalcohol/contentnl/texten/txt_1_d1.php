
<p>Slecht slapen hoort er bij.</p>
<p>&nbsp;</p>
<p>Zeer waarschijnlijk ga je wat slechter slapen, de eerste dagen. Dat is ook 
  logisch want jouw lichaam mist de regelmaat in de toevoer van de downer die alcohol toch chemisch gezien ook is.. Voor 
  een hoop mensen is dit de reden om dan maar weer te gaan drinken op de bekende 
  manier... Niet handig want dan blijft dat probleem bestaan en er is meer over 
  te zeggen...<br>
  Als je namelijk wat beter gaat kijken naar slaap en alcohol dan blijkt dat iemand 
  door alcohol juist minder goed slaapt!<br>
  Niet alleen zie je duidelijk dat mensen die 's avonds drinken er vaker uit zullen 
  moeten voor een nachtelijk plasje (ook al is alcohol een vloeistof, het heeft 
  toch een vochtafdrijvende werking).<br>
  Daanaast zie je wat je bij andere downers ook ziet... Iemand is misschien niet 
  wakker... maar &quot;niet-wakker-zijn&quot; is echt iets anders dan &quot;wel 
  rustig Slapen&quot;. De nachtrust van mensen die zich verdoven is minder volledig, 
  met name de REM-slaap, waarin we de dingen 'op een rij' lijken te zetten voor 
  onszelf die er hebben plaats gevonden, die is minder goed. Dat is heel duidelijk 
  zichtbaar bij mensen die, tijdens een experiment telkens in hun slaap gestoord 
  worden als ze in de REM-fase komen van hun slaap. Wat blijkt? Daar kan je al 
  heel snel behoorlijk raar van worden...<br>
  Goed, het slapen is misschien minder goed door alcohol maar INslapen is niet 
  altijd makkelijk wanneer je het regelmatige gebruik stopt. Je maakt natuurlijk 
  in deze dagen wel een echte stap. Een lichaam haalt de alcohol van gister natuurlijk 
  wel voor een goed deel uit jouw systeem. Maar het starten met de ontwenning van 
  een verslaving is natuurlijk wel een moment dat je voelt dat jouw lichaam even 
  wat makkelijker uit balans is dan normaal. Lichamelijk gezien, biologisch bekeken, 
  is de stap van drinker naar niet-drinker natuurlijk een duidelijke chemische 
  verstoring van de balans. Jouw lichaam is voorheen in een soort van biologisch 
  'evenwicht' gekomen met de regelmatige vergiftiging. In jouw lichaam, dat zich 
  aan de regelmatige toediening van dit zenuwgif heeft aangepast, ervaren we de 
  lichamelijke ontwenning natuurlijk als een verstoring.<br>
  Sommige mensen hebben dan net een beetje meer last van het gevoel licht in hun 
  hoofd te worden, duizeligheid, slapeloosheid, concentratiestoornissen, angsten 
  of lichamelijke klachten.<br>
  Ook niet echt dingen die de nachtrust bevorderen. En dan hebben we het nog niet 
  eens gehad over de biologie onder de psychologische kanten van een ontwenningsperiode.<br>
  Wanneer je echt last blijft houden van slaapstoringen, dan kun je natuurlijk 
  even contact opnemen met jouw huisarts...
