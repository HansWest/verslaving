<b>denken over terugval</b><br>
Niet om je te ontmoedigen. Maar ik zou je graag (kort!) laten nadenken over een reactie op 'terugval': als je een uitglijer maakt, en misschien zelfs serieus omvalt... 
                      &quot;Dat kan. Maar let er dan op dat je niet helemaal terugvalt.&quot; 
                      &quot;Hoe kan je herkennen dat je aan het afglijden bent?&quot; 
                      en<br>
                      &quot;Wie kan je helpen om te bereiken dat je weer opstaat?&quot;</p>
                    <p><br>
                      Een terugval is te gebruiken als een les: &quot;Wat zijn 
                      mijn valkuilen?&quot; (en reken er maar op dat je ze hebt... 
