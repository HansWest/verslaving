<p><b>wat is dat nou: computer-coachen</b><br>
  Natuurlijk kan een computer nooit bieden wat een menselijke coach te bieden 
  heeft (als het een goeie is). Het is immers zo dat er niets zo ingewikkeld is 
  om te zien als je eigen blinde vlekken...<br>
  Maar een computer als 'coach' heeft natuurlijk het voordeel dat je geheime gedachten 
  wat makkelijker kan verwoorden en vooral: je doet het zelf. Het zijn jouw acties 
  die maken dat er iets in jouw leven verandert.<p>
 
  <b>coachen</b><br>
  Het is een woordje dat je tegnwoordig gelukkig meer tegenkomt: 'caoching'. Dus even een klein woordje over 'coachen'.<br>
  Een coach probeert je te begeleiden in de praktische zin. Hoe kan je het 't 
  beste aanpakken, hoe kan je resources binnen jouw bereik het beste inzetten 
  voor het bereiken van jouw doelen... en hoe kan je verstandige doelen stellen. 
  Kortom, een coach helpt je bij de vraag: &quot;hoe kan jij jezelf het beste 
  helpen?&quot;<br>
  Coaching is gericht op het hier-en-nu en de toekomst. Natuurlijk heeft de manier waarop wij nu tegen de wereld aankijken van alles 
  te maken met de manier waarop we in ons verleden hebben geleerd teggen de wereld 
  aan te kijken. Maar een coach zal niet de nadruk gaan leggen op dat verleden. Het feit dat 
  je dit zit te lezen betekent dat je gezond genoeg bent om interesse te ontwikkelen 
  voor stappen voorwaarts. Dus als we naar verleden kijken, laten we dat dan doen 
  in het licht van de toekomst.</p>
