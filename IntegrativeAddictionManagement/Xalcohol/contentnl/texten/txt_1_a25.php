<p>Dit is en periode waarin je alleen maar bij hoeft te houden hoeveel je drinkt, 
  wanneer je drinkt en waarom je drinkt. Het is werkelijk niet de bedoeling dat 
  je minder zou moeten gaan drinken, ofzo (hoewel het wel regelmatig een gevolg 
  is wanneer mensen hun gebruik bij gaan houden).<br>
  Het draait erom dat jij overzicht krijgt over de redenen en de manieren waarop 
  je drinkt.<br>
  Dat is voor veel mensen een beetje een saaie periode. Je lijkt nog niet aan 
  het werk te zijn en &quot;je weet toch zeker wel waarom je drinkt&quot;...<br>
  Als ik bot mag zijn: forget it! Keer op keer maak ik mee dat mensen die en verslaving 
  beginnen te ontwennen ineens bemerken dat zij toch andere redenen hadden voor 
  hun gebruik dan dat zij zelf gedacht hadden.<br>
  Keer op keer maak ik mee dat er achteraf meer lagen bleken te zitten in het 
  probleem van de verslaving dan dat mensen in de gaten hadden. Niet omdat zij 
  daar niet slim genoeg voor waren maar omdat een verslaving er nou eenmaal voor 
  zorgt dat dit soort oorzaken voor gebruik overdekt worden door het gebruik zelf.<br>
  Die waarom-zou-ik-willen-drinken-redenen, die worden later heel belangrijk want 
  daar kun je alternatieven voor gaan verzinnen. Dus wees nou niet zo snel met 
  het antwoord met het antwoord &quot;da's door m'n verslaving&quot;. Je zou kunnen 
  stellen dat een praktische definitie van 'verslaving' is: &quot;dat een mens 
  niet WEET waarom ie wil drinken&quot;<br>
  Dus doe jezelf een plezier en wees consequent. Probeer geen dag over te slaan 
  en houdt dagelijks bij wat je wanneer drinkt, welke gedachten en gevoelens je 
  daarbij hebt en dus waarom je uiteindelijk die eenheid genuttigd hebt.</p>
<p> Nog even over eenheden:<br>
  Schrijf het gewoon op in normale eenheden. &quot;Nee, een longdrinkglas whiskey 
  is dus niet(!) EEN glaasje&quot;. Het lijkt misschien flauw dat ik dat zo zeg. 
  Maar ik zou edcht heel graag een euro krijgen voor iedere keer dat ik ben tegengekomen 
  dat mensen zich op deze manier voor het lapje proberen te houden. Daar kan ik 
  snel een lang weeken Parijs integraal van betalen.<br>
  Gewoon de eenheden noteren zoals die in een normale kroeg worden uitgeschonken.</p>
