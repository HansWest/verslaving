Het is ook wel handig om tevoren vast te weten dat een verslaafd brein nou eenmaal 
de neiging krijgt om smoezen te gaan verzinnen om te mogen gebruiken. De onrust 
van de trek borrelt als het ware vanuit de hersenstam omhoog in jouw brein en vormt 
allerlei gevoelens en gedachten.<br>
Gedachten aan sigarettten aan de ene kant. Da's lastig maar dat gaat vanzelf over. 
Je zult waarschijnlijk nog wel een aantal keer aan sigaretten beginnen te denken. 
Dat maakt niet uit. Je moet alleen niet de fout maken om daarover door te denken.<br>
Lastiger nog is dat een verslaafd brein de neiging krijgt om smoezen te gaan maken 
waarom het logisch is om er toch maar weer eentje op te steken. Ik zeg altijd: 
smoezen en gedachten die de achterdeur dreigen open te zetten. <br>
Je zult in de lopende dagen gedachten krijgen die het 'logisch' maken om de achterdeur 
naar gebruik weer open te zetten. Het heeft niet zoveel zin om tegen jezelf te 
zeggen dat je dat niet zult doen want het gebeurt 99,9% van de mensen die een 
verslaving doorbreken.<br>
