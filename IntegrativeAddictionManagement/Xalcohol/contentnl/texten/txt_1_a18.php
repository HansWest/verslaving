<b>agendaplanning</b><br>
Planning is vaak noodzakelijk om op de lange termijn ook te laten gebeuren 
  wat jij wenst dat er gebeurt.<br>
  Niet altijd makkelijk wat mensen laten nog wel eens hun leven leiden door de 
  dingen die er zo van dag-tot-dag gebeuren. Dat geldt voor iedereen maar dat geldt 
  vaak zeker voor mensen die ongemerkt even in de valkuil van een verslaving gestapt 
  zijn.<br>
  Vaak is het zo, als je merkt dat je toch echt wat minder MOET gaan drinken, 
  dat er toch al wel wat dingen mis beginnen te lopen in een leven. Zelden dat 
  iemand zegt: &quot;mijn leven blijft helemaal goed lopen, geen vuiltje aan de 
  lucht, ik moet nodig eens wat minder gaan drinken&quot;... Het komt natuurlijk 
  ook wel regelmatig voor dat de trek die vanuit de hersenstam komt ervoor zorgt 
  dat de aandacht voor de belangrijke dingen in het dagelijks leven wat verslapt 
  is.<br>
  Maar er zijn ook andere dingen belangrijk bij het plannen van de dagen. Een 
  dagplanning is namelijk een prachtige methode om tot zelfinizcht te komen. je 
  kunt je heel krampachtig aan de planning vast gaan houden die je jezelf bedacht 
  had. Je kunt ook zeggen: &quot;blijkbaar heb ik nou toch erg veel zin om het 
  anders te laten lopen dan dat ik gedacht had...Dit is interessant! Wat heb 
  ik nou niet bedacht, waar heb ik nou geen rekening mee gehouden toen ik deze 
  planning maakte?&quot; Misschien had je meer rust moeten plannen, of misschien 
  had je meer actie moeten plannen. Misschien had je bepaalde mensen niet moeten 
  tegenkomen of misschien had je beter zo kunnen plannen dat bepaalde mensen <i>uit </i>je buurt zouden blijven of dat jij niet daar zou komen waar zij wel waren...<br>
  Schaf een agenda aan en leer 'm voor jouw zelf gebruiken.<br>
  <span class='rechtsbordertje'><img src='../pics/macsmiley.gif' border='0'> Wat is management zonder agenda's?<br>
  Waarom zou self-management dan wel zonder agenda kunnen...</span> <br>
Plaats niet alleen de verplichtingen in je agenda, de dingen die je moet(!) doen 
maar plan ook de tijd vast waarin je leuke dingen gaat doen, plezier gaat beleven, 
beloningen mag incasseren.</p> 
<p> Plan je dagen en leer jezelf kennen...</p> 

